import { NextResponse } from 'next/server';

export const dynamic = 'force-static';

const UPSTREAM_URL = 'https://static.cloudflareinsights.com/beacon.min.js';

/**
 * First-party proxy for Cloudflare Web Analytics beacon.
 *
 * Why: Some privacy tools block third-party script origins. Serving via a
 * first-party endpoint improves reliability while keeping the upstream source
 * authoritative.
 */
export async function GET() {
  const upstream = await fetch(UPSTREAM_URL, {
    cache: 'force-cache',
  });

  if (!upstream.ok || !upstream.body) {
    return new NextResponse('/* Cloudflare beacon unavailable */\n', {
      status: 502,
      headers: {
        'content-type': 'application/javascript; charset=utf-8',
        'cache-control': 'no-store',
      },
    });
  }

  const headers = new Headers(upstream.headers);
  headers.set('content-type', 'application/javascript; charset=utf-8');
  headers.set(
    'cache-control',
    'public, max-age=3600, s-maxage=86400, stale-while-revalidate=604800'
  );
  headers.delete('set-cookie');

  return new NextResponse(upstream.body, {
    status: upstream.status,
    headers,
  });
}
